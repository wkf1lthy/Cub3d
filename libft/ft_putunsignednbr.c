#include "libft.h"

int	ft_putunsignednbr(long n)
{
	char	c;
	int		i;

	i = 1;
	if (n >= 10)
		i += ft_putnbr(n / 10);
	c = (n % 10) + '0';
	ft_putchar(c);
	return (i);
}
